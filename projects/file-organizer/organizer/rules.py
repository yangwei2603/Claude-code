def get_default_rules() -> dict:
    """获取默认规则的深拷贝，优先从 templates/digital-office.json 加载"""
    # templates 在项目根目录下，不是 organizer 包内
    template_path = Path(__file__).parent.parent / "templates" / "digital-office.json"
    if template_path.exists():
        try:
            with open(template_path, "r", encoding="utf-8") as f:
                return copy.deepcopy(json.load(f))
        except Exception:
            pass
    return copy.deepcopy(DEFAULT_RULES)


